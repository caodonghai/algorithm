// 总共n阶楼梯，每次只能迈1步或者2步，问走上n阶楼梯总共有多少种方案？

// 解题：
// 1阶：1种
// 2阶：2种 = (1阶方案 + 走2步) + 走2步
// 3阶：3种 = (2阶方案 + 走1步) + (1阶方案 + 走2步)
// 4阶：5种 = (3阶方案 + 走1步) + (2阶方案 + 走2步)
// ...

function totalFuns(n) {
    let dp = [1, 1, 2]
    if(n <= 2) return dp[n]
    for (let i = 3; i <= n; i++) {
        dp[i] = dp[i-1] + dp[i-2]
    }
    console.log({dp});
    return dp[n]
}